y(1:2,1:5)=zeros(2,5)+0.1*randn(2,5);
for t=2:2000
    w=0.1*randn(5,1);
    y(t,1)=0.2*y(t-1,1)-0.4*y(t-1,2)+w(1);
    y(t,2)=-0.5*y(t-1,1)+0.15*y(t-1,2)+w(2);
    y(t,3)=-0.14*y(t-1,2)+w(3);
    y(t,4)=0.5*y(t-1,1)-0.25*y(t-1,2)+w(4);
    y(t,5)=0.15*y(t-1,1)+w(5);
end
%
csvwrite('y.csv',y,0,1)